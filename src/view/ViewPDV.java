    private void salvarVenda() {
        int codigoVenda = 0;
        mv = new ModelVenda();
        mv.setIdcliente(CodCli=vPagPDV.CodCLi);
        mv.setDesconto(vPagPDV.getDesconto());
        mv.setValor(vPagPDV.getSubtotal());
        mv.setValortotal(Double.parseDouble(vPagPDV.LbTotal.getText()));

        codigoVenda = cv.salvarvendaController(mv);
        int cont = TbItens.getRowCount();

        for (int i = 0; i < cont; i++) {
            int codigoP = (int) TbItens.getValueAt(i, 1);
            miv = new ModelItensVenda();
            mp = new ModelProduto();
            miv.setCodproduto(codigoP);
            miv.setCodvenda(codigoVenda);
            miv.setItensvalor((double) TbItens.getValueAt(i, 3));
            miv.setIntensqtd(Integer.parseInt(TbItens.getValueAt(i, 4).toString()));

            //Produto
            mp.setIdProduto((int) TbItens.getValueAt(i, 1));
            mp.setProEstoque(cp.listarProduto(codigoP).getProEstoque() - Integer.parseInt(TbItens.getValueAt(i, 4).toString()));
            listaIV.add(miv);
            listaProduto.add(mp);

        }
        if (civ.salvarItensVendaController(listaIV)) {
            cp.alterarEstoqueProdutoController(listaProduto);
            JOptionPane.showMessageDialog(this, "Venda Efetuada com Sucesso!", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
            this.limpar();

        } else {
            JOptionPane.showMessageDialog(this, "Venda N√£o Efetuada!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    private double somarTotal() {
        double soma = 0, valor = 0;
        int cont = TbItens.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = Double.parseDouble(String.valueOf(TbItens.getValueAt(i, 5)));
            soma += valor;
        }

        return soma;
    }

    private void pegarDados(java.awt.event.KeyEvent evt) {
        DefaultTableModel modelo = (DefaultTableModel) TbItens.getModel();
        if (evt.getKeyCode() == java.awt.event.KeyEvent.VK_ENTER) {
            try {
                if (!TxtCodigo.getText().equals("")) {
                    String qtda = JOptionPane.showInputDialog(this, "Digita a Quantidade", "Informa√ß√£o", JOptionPane.OK_OPTION);
                    mp = cp.listarProduto(Integer.parseInt(TxtCodigo.getText()));
                    modelo.addRow(new Object[]{
                        modelo.getRowCount() + 1,
                        mp.getIdProduto(),
                        mp.getProNome(),
                        mp.getProValor(),
                        qtd = Integer.parseInt(qtda),
                        mp.getProValor() * qtd
                    });
                    LbBruto.setText("" + somarTotal());
                    TxtCodigo.setText("");
                } else {
                    JOptionPane.showMessageDialog(this, "Voce deve informar o codigo do produto", "Informa√ß√£o", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {

            }
        }

    }

