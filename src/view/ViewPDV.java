    public void imprimir(String texto){
        try {
            InputStream print= new ByteArrayInputStream(texto.getBytes());
            DocFlavor doflavor=DocFlavor.INPUT_STREAM.AUTOSENSE;
            SimpleDoc doctexto= new SimpleDoc(print, doflavor, null);
            PrintService ps= PrintServiceLookup.lookupDefaultPrintService();
            
            PrintRequestAttributeSet pa= new HashPrintRequestAttributeSet();
            pa.add(new JobName("Impress√£o", null));
            pa.add(OrientationRequested.PORTRAIT);
            pa.add(MediaSizeName.ISO_A4);
            
            DocPrintJob docpj=ps.createPrintJob();
            try {
                docpj.print(doctexto, pa);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "N√£o foi possivel imprimir", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            print.close();
            
        } catch (Exception e) {
        }
        
    }
