    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int cli = TbProduto.getSelectedRow();
        int confirma = JOptionPane.showConfirmDialog(this, "Tem certesa que deseja eliminar : " + TbProduto.getValueAt(cli, 1) + "?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirma == JOptionPane.YES_OPTION) {
            int linha = TbProduto.getSelectedRow();
            int codigo = (int) TbProduto.getValueAt(linha, 0);
            if (cp.eliminarControllerProduto(codigo)) {
                JOptionPane.showMessageDialog(this, "Produto Eliminado com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                this.carregarProduto();
                this.bloquerBotoes();
            } else {
                JOptionPane.showMessageDialog(this, "Produto Não Eliminado!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

