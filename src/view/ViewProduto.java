    private void BtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAlterarActionPerformed
        salvarAlterar = "Alterar";
        int linha = TbProduto.getSelectedRow();
        try {
            int codigo = (int) TbProduto.getValueAt(linha, 0);
            mp = cp.listarProduto(codigo);
            TxtCodigoProd.setText(String.valueOf(mp.getIdProduto()));
            TxtNomeProd.setText(mp.getProNome());
            TxtEstoque.setText(String.valueOf(mp.getProEstoque()));
            TxtValor.setText(String.valueOf(mp.getProValor()));
            this.desbloquear();
            BtnAlterar.setEnabled(false);
            BtnCancelar.setEnabled(true);
            BtnEliminar.setEnabled(false);
            BtnSalvar.setEnabled(true);
            BtnNovo.setEnabled(false);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Seleciona o Produto a Ser Alterado! ", "Informação", JOptionPane.INFORMATION_MESSAGE);
        }


    }//GEN-LAST:event_BtnAlterarActionPerformed

