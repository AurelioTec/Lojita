    private void limparBloquear() {
        TxtEstoque.setText("");
        TxtNomeProd.setText("");
        TxtValor.setText("");
        TxtCodigoProd.setText("");
        TxtPesquisar.setText("");
        TxtEstoque.setEnabled(false);
        TxtNomeProd.setEnabled(false);
        TxtValor.setEnabled(false);
        TxtCodigoProd.setEnabled(false);
    }

    private void bloquerBotoes() {
        BtnNovo.setEnabled(true);
        BtnAlterar.setEnabled(false);
        BtnCancelar.setEnabled(false);
        BtnEliminar.setEnabled(false);
        BtnSalvar.setEnabled(false);
        BtnPesquisar.setEnabled(false);
    }

    private void desbloquear() {
        TxtEstoque.setEnabled(true);
        TxtNomeProd.setEnabled(true);
        TxtValor.setEnabled(true);
    }

    private void carregarProduto() {
        lista = cp.listaProdutoControllerProduto("");
        DefaultTableModel modelo = (DefaultTableModel) TbProduto.getModel();
        modelo.setNumRows(0);
        int cont = lista.size();
        for (int i = 0; i < cont; i++) {
            modelo.addRow(new Object[]{
                lista.get(i).getIdProduto(),
                lista.get(i).getProNome(),
                lista.get(i).getProValor(),
                lista.get(i).getProEstoque()
            });
        }
    }

    public void salvarProduto() {
        mp.setProNome(TxtNomeProd.getText());
        mp.setProEstoque(Integer.parseInt(TxtEstoque.getText()));
        mp.setProValor(ut.virgulaParaPonto(TxtValor.getText()));
        if (cp.cadastrarControllerProdutos(mp) > 0) {
            JOptionPane.showMessageDialog(this, "Produto Cadastrdo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            this.carregarProduto();
            this.limparBloquear();
            this.bloquerBotoes();
        } else {
            JOptionPane.showMessageDialog(this, "Produto Não Cadastrdo!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

    public void pesquisar() {
        DefaultTableModel dfm = (DefaultTableModel) TbProduto.getModel();
        dfm.setNumRows(0);
        for (ModelProduto mps : cp.listaProdutoControllerProduto(TxtPesquisar.getText())) {
            Object[] dados = {mps.getIdProduto(), mps.getProNome(), mps.getProEstoque(), mps.getProValor()};
            dfm.addRow(dados);
        }
    }

    public void alterarProduto() {
        mp.setProNome(TxtNomeProd.getText());
        mp.setProEstoque(Integer.parseInt(TxtEstoque.getText()));
        mp.setProValor(ut.virgulaParaPonto(TxtValor.getText()));
        if (cp.alterarControllerProduto(mp)) {
            JOptionPane.showMessageDialog(this, "Produto Alterado com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            this.carregarProduto();
            this.limparBloquear();
            this.bloquerBotoes();
        } else {
            JOptionPane.showMessageDialog(this, "Produto Não Alterado!", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }

