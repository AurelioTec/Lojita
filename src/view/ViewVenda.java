    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        double soma = 0, valor;
        int codigoVenda = 0;
        listaIV = new ArrayList<>();
        int cont = TbProduto.getRowCount();
        for (int i = 0; i < cont; i++) {
            valor = (double) TbProduto.getValueAt(i, 4);
            soma = soma + valor;
        }
        if (!TxtCodVenda.getText().equals("")) {
            mv.setIdvenda(Integer.parseInt(TxtCodVenda.getText()));
        }
        mv.setIdcliente(Integer.parseInt(TxtCodClie.getText()));
        mv.setValor(soma);
        mv.setDesconto(Double.parseDouble(TxtDesconto.getText()));
        mv.setValortotal(Double.parseDouble(TxtValotTotal.getText()));

        if (alterarSalvar.equals("salvar")) {
            //Salvar a venda

            codigoVenda = cv.salvarvendaController(mv);
            if (codigoVenda > 0) {
                JOptionPane.showMessageDialog(this, "Venda Salva com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "venda Não Salva!", "Erro", JOptionPane.ERROR_MESSAGE);
            }

            for (int i = 0; i < cont; i++) {
                int codigoP = (int) TbProduto.getValueAt(i, 0);
                miv = new ModelItensVenda();
                mp = new ModelProduto();
                miv.setCodproduto(codigoP);
                miv.setCodvenda(codigoVenda);
                miv.setItensvalor((double) TbProduto.getValueAt(i, 3));
                miv.setIntensqtd(Integer.parseInt(TbProduto.getValueAt(i, 2).toString()));

                //Produto
                mp.setIdProduto((int) TbProduto.getValueAt(i, 0));
                mp.setProEstoque(cp.listarProduto(codigoP).getProEstoque() - Integer.parseInt(TbProduto.getValueAt(i, 2).toString()));
                listaIV.add(miv);
                listaProduto.add(mp);
            }
            if (civ.salvarItensVendaController(listaIV)) {
                cp.alterarEstoqueProdutoController(listaProduto);
                // JOptionPane.showMessageDialog(this, "Itens da Venda Salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                this.carregarVenda();
                this.limparCampos();
                TPVenda.setSelectedIndex(1);
            } else {
                JOptionPane.showMessageDialog(this, "Itens da Venda Não Salvo!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            //Alterar

            //Retornar os itens ao estoque:
            int linha = TbVenda.getSelectedRow();
            codigoVenda = (int) TbVenda.getValueAt(linha, 0);
            listaProduto = new ArrayList<>();
            listaIVP = cIVP.getListaItensvendaProdutoDao(codigoVenda);
            for (int i = 0; i < listaIVP.size(); i++) {
                mp = new ModelProduto();
                mp.setIdProduto(listaIVP.get(i).getMprodutos().getIdProduto());
                mp.setProEstoque(listaIVP.get(i).getMprodutos().getProEstoque() + listaIVP.get(i).getmItensVenda().getIntensqtd());
                listaProduto.add(mp);
            }
            if (cp.alterarEstoqueProdutoController(listaProduto)) {

                if (civ.eliminarItensVendaController(codigoVenda)) {

                    //  JOptionPane.showMessageDialog(this, "Itens da Venda Eliminada com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    this.carregarVenda();

                } else {
                    JOptionPane.showMessageDialog(this, "Itens da Venda Não Eliminada!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Venda Não Eliminada!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            //Fim Retornar os itens ao estoque:

            if (cv.atualizarvendaController(mv)) {
                JOptionPane.showMessageDialog(this, "Venda Alterada com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(this, "Venda Não Alterada", "Erro", JOptionPane.ERROR_MESSAGE);
            }
            for (int i = 0; i < cont; i++) {
                int codigoP = (int) TbProduto.getValueAt(i, 0);
                miv = new ModelItensVenda();
                mp = new ModelProduto();
                miv.setCodproduto(codigoP);
                miv.setCodvenda(codigoVenda);
                miv.setItensvalor((double) TbProduto.getValueAt(i, 3));
                miv.setIntensqtd(Integer.parseInt(TbProduto.getValueAt(i, 2).toString()));

                //Produto
                mp.setIdProduto((int) TbProduto.getValueAt(i, 0));
                mp.setProEstoque(cp.listarProduto(codigoP).getProEstoque() - Integer.parseInt(TbProduto.getValueAt(i, 2).toString()));
                listaIV.add(miv);
                listaProduto.add(mp);
            }
            if (civ.salvarItensVendaController(listaIV)) {
                // JOptionPane.showMessageDialog(this, "Itens da Venda Salvo com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                this.carregarVenda();
                this.limparCampos();
            } else {
                JOptionPane.showMessageDialog(this, "Itens da Venda Não Salvo!", "Erro", JOptionPane.ERROR_MESSAGE);
            }

        }
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void BtnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAlterarActionPerformed
        alterarSalvar = "alterar";
        BtnCancelar.setEnabled(true);
        BtnSalvar.setEnabled(true);
        BtnNovo.setEnabled(false);
        int linha = TbVenda.getSelectedRow();
        int codigoVenda = (int) TbVenda.getValueAt(linha, 0);
        listaIVP = cIVP.getListaItensvendaProdutoDao(codigoVenda);
        DefaultTableModel modelo = (DefaultTableModel) TbProduto.getModel();
        modelo.setNumRows(0);
        for (int i = 0; i < listaIVP.size(); i++) {
            TxtCodVenda.setText("" + listaIVP.get(i).getmItensVenda().getCodvenda());
            modelo.addRow(new Object[]{listaIVP.get(i).getMprodutos().getIdProduto(), listaIVP.get(i).getMprodutos().getProNome(), listaIVP.get(i).getmItensVenda().getIntensqtd(), listaIVP.get(i).getmItensVenda().getItensvalor(), listaIVP.get(i).getmItensVenda().getItensvalor() * listaIVP.get(i).getmItensVenda().getIntensqtd()});
        }
        this.somaValorTotal();
        TPVenda.setSelectedIndex(0);
    }//GEN-LAST:event_BtnAlterarActionPerformed

    private void BtnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnEliminarActionPerformed
        int venda = TbVenda.getSelectedRow();
        int confirma = JOptionPane.showConfirmDialog(this, "Tem certesa que deseja eliminar a venda: " + TbVenda.getValueAt(venda, 0) + "?", "Atenção", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirma == JOptionPane.YES_OPTION) {
            int linha = TbVenda.getSelectedRow();
            int codigoVenda = (int) TbVenda.getValueAt(linha, 0);
            listaProduto = new ArrayList<>();
            listaIVP = cIVP.getListaItensvendaProdutoDao(codigoVenda);
            for (int i = 0; i < listaIVP.size(); i++) {
                mp = new ModelProduto();
                mp.setIdProduto(listaIVP.get(i).getMprodutos().getIdProduto());
                mp.setProEstoque(listaIVP.get(i).getMprodutos().getProEstoque() + listaIVP.get(i).getmItensVenda().getIntensqtd());
                listaProduto.add(mp);
            }
            if (cp.alterarEstoqueProdutoController(listaProduto)) {
                civ.eliminarItensVendaController(codigoVenda);
                if (cv.excluirvendaController(codigoVenda)) {
                    JOptionPane.showMessageDialog(this, "Venda Eliminada com Sucesso!", "Informação", JOptionPane.INFORMATION_MESSAGE);
                    this.carregarVenda();

                } else {
                    JOptionPane.showMessageDialog(this, "Venda Não Eliminada!", "Erro", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(this, "Venda Não Eliminada!", "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_BtnEliminarActionPerformed

