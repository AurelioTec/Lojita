    private void BtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnAddActionPerformed
        if (TxtQtd.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Campos Vazios! Prerncha os campos vazios!", "Atenção", JOptionPane.WARNING_MESSAGE);
        } else {
            TxtDesconto.setEnabled(true);
            TxtDesconto.setText("" + 0);
            mp = cp.listarProduto(Integer.parseInt(TxtCodProd.getText()));
            DefaultTableModel modelo = (DefaultTableModel) TbProduto.getModel();
            int cont = 0;
            double qtd = 0;
            qtd = Double.parseDouble(TxtQtd.getText());
            for (int i = 0; i < cont; i++) {
                modelo.setNumRows(0);
            }
            modelo.addRow(new Object[]{
                mp.getIdProduto(),
                mp.getProNome(),
                TxtQtd.getText(),
                mp.getProValor(),
                qtd * mp.getProValor()
            });
            this.somaValorTotal();
        }
        BtnSalvar.setEnabled(true);
    }//GEN-LAST:event_BtnAddActionPerformed

