    private void TxtValorRecebidoFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_TxtValorRecebidoFocusLost
        if (TxtValorRecebido.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Você deve Inserir o valor entregue pelo cliente", "Atenção", JOptionPane.WARNING_MESSAGE);
            TxtValorRecebido.requestFocus();

        } else {
            if (Double.parseDouble(TxtValorRecebido.getText()) < (Double.parseDouble(TxtSubtotal.getText()) - Double.parseDouble(Txtdesconto.getText()))) {
                JOptionPane.showMessageDialog(this, "Valor Recebido não corresponde ao valor a pagar", "Atenção", JOptionPane.WARNING_MESSAGE);
            } else {
                calcularPagamento();
            }
        }
    }//GEN-LAST:event_TxtValorRecebidoFocusLost

