    private void listarCliente() {
        listaCliente = cc.listaClienteControllerCliente("");
        CbNomeCli.removeAllItems();
        for (int i = 0; i < listaCliente.size(); i++) {
            CbNomeCli.addItem(listaCliente.get(i).getCliNome());

        }
    }

    public void carregarComboCliente() {
        mdc = cc.listarClienteNome(CbNomeCli.getSelectedItem().toString());
        CodCLi = mdc.getIdCliente();
    }

    private void calcularPagamento() {
        double desco, subto, pagar, receber = 0, trocos;
        subto = Double.parseDouble(TxtSubtotal.getText());
        if (!Txtdesconto.getText().equals("")) {
            desco = Double.parseDouble(Txtdesconto.getText());
        } else {
            desco = 0;
        }
        if (!TxtValorRecebido.getText().equals("")) {
            receber = Double.parseDouble(TxtValorRecebido.getText());
        } else {
            TxtValorRecebido.requestFocus();
        }
        pagar = subto - desco;
        LbTotal.setText(pagar + "");

        trocos = receber - pagar;
        LbTroco.setText(trocos + "");
    }

    private void listarFormaPagamento() {
        listaMFP = CFP.listaControllerFormaPagamento("");
        CBPagamento.removeAllItems();
        for (int i = 0; i < listaMFP.size(); i++) {
            CBPagamento.addItem(listaMFP.get(i).getDescripag());

        }
    }

